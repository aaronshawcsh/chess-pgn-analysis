public class Analysis {
    private double white, black;
    public Analysis(double white, double black) {
        this.white = white;
        this.black = black;
    }

    public double getWhite() {
        return white;
    }

    public void setWhite(double white) {
        this.white = white;
    }

    public double getBlack() {
        return black;
    }

    public void setBlack(double black) {
        this.black = black;
    }
}
